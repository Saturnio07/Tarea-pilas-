public class Trastes 
{
    String traste;
    int cubiertos;
    
    public Trastes(String traste, int cubiertos)
    {
        this.traste = traste;
        this.cubiertos = cubiertos;
    }

    public String getTraste() {
        return traste;
    }

    public void setTraste(String traste) {
        this.traste = traste;
    }

    public int getCubiertos() {
        return cubiertos;
    }

    public void setCubiertos(int cubiertos) {
        this.cubiertos = cubiertos;
    }
    
    
}
